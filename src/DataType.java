public class DataType{
    int num;
    String name;
    float num1;
    short num2;
    double num3;
    long num4;
    boolean var;
    char ch;
    public static void main(String[] args){
        DataType m=new DataType();

        System.out.println("int:" + m.num);
        System.out.println("String:" + m.name);
        System.out.println("float:"+ m.num1);
        System.out.println("short:" + m.num2);
        System.out.println("double:" + m.num3);
        System.out.println("long:" + m.num4);
        System.out.println("boolean:" + m.var);
        System.out.println("char:" + m.ch);

    }
}
